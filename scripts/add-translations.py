def update_locale(locale: str, translations: dict) -> None:
    """Update a locale file with the new translations."""
    path = MESSAGES_DIR / f"{locale}.json"
    if not path.exists():
        print(f"⚠️  {path} does not exist — skipping")
        return

    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)

    # For each namespace, add/update keys
    for namespace, keys in translations.items():
        if namespace not in data:
            data[namespace] = {}
        for k, v in keys.items():
            # Don't overwrite existing translations (already in en.json for English)
            if k not in data[namespace] or locale != "en":
                data[namespace][k] = v
            # For English, we keep our updated version (already set)

    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write("\n")

    print(f"✓ Updated {path}")


if __name__ == "__main__":
    for locale, translations in TRANSLATIONS.items():
        update_locale(locale, translations)
    print("\nDone. All locale files updated.")
